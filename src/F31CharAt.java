
public class F31CharAt {

	public static void main(String[] args) {
		String name = "Irina";
		name.charAt(0);
		System.out.println(name.charAt(0));
		System.out.println(name.charAt(2));
		
		"Irina".charAt(0);
		System.out.println("Irina".charAt(1));
		System.out.println("Irina".charAt(4));
		System.out.println("Irina".charAt(3));
		
		char ch = name.charAt(0);
		System.out.println(ch);

	}

}
